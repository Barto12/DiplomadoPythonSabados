#Accesamos a elementos de la lista por indice

numeros = [10, 20, 30, 40]

print("Primer elemento:", numeros[0])
print("Ultimo elemento:", numeros[-1])

# Explicacion
# numeros[0] accede al primer elemento
# numeros[-1] accede al ultimo elemento (indexacion negativa)

# Modificar elementos

numeros = [10, 20, 30, 40]
numeros[2] = 99
print(numeros)

# Explicacion
# las listas son mutables y podemos cambiar sus valores usando directamente el indice

